package com.frc5823.team5823.managers;

public class CoordinateFrameManager {
  /*
   * CoordinateFrameManager helps with transform coordinate frames throughout
   * the match. A coordinate frame is simply a point and direction in space that
   * defines an (x,y) coordinate system. Transforms (or poses) keep track of the
   * spatial relationship between different frames.
   *
   * Robot frames of interest (from parent to child):
   *
   * 1. Field frame: origin is decided by Robot-Automation.
   *
   * 2. Hub frame: origin is the center of the hub.
   *
   * 3. Vehicle frame: origin is the center of the robot wheelbase, facing
   * forwards
   *
   * 4. Turret frame: origin is the center of the turret.
   *
   * 5. Camera frame: origin is the center of the camera.
   */
}
